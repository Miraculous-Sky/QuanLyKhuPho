    private void btnLuuNguoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuNguoiActionPerformed
        String maNguoi = txfMaNguoi.getText();
        String hoTen = txfHoTen.getText();
        int namSinh = Integer.parseInt(txfNamSinh.getText());
        int tuoi = Integer.parseInt(txfTuoi.getText());
        String ngheNghiep = txfNgheNghiep.getText();
        int affectedRow = Nguoi.edit(maNguoi, hoTen, tuoi, namSinh, ngheNghiep);
        switch (affectedRow) {
            case -1:
                JOptionPane.showMessageDialog(this, "Có lỗi trong quá trình, hãy thử lại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                break;
            case 0:
                JOptionPane.showMessageDialog(this, "Mã người không tồn tại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Cập nhật thông tin người thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadHoDan();
                loadTable();
                break;
        }
    }//GEN-LAST:event_btnLuuNguoiActionPerformed

