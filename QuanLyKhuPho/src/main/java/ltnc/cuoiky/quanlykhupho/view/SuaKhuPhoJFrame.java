    private void btnLuuHoDanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuHoDanActionPerformed
        String maHoDan = txfMaHoDan.getText();
        String soNha = txfSoNha.getText();
        int affectedRow = HoDan.edit(maHoDan, soNha);
        switch (affectedRow) {
            case -1:
                JOptionPane.showMessageDialog(this, "Có lỗi trong quá trình, hãy thử lại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                break;
            case 0:
                JOptionPane.showMessageDialog(this, "Mã hộ dân không tồn tại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Cập nhật thông tin hộ dân thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadKhuPho();
                break;
        }
    }//GEN-LAST:event_btnLuuHoDanActionPerformed

