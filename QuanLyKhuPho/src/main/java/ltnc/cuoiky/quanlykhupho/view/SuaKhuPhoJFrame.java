    private void loadKhuPho() {
        KHUPHO khuPho = KhuPho.getByMaKhuPho(maKhuPho);
        if (khuPho == null) {
            JOptionPane.showMessageDialog(this, "Mã khu phố không hợp lệ hoặc xảy ra lỗi, thử lại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            new QLKhuPhoJFrame().setVisible(true);
            return;
        }
        txfMaKhuPho.setText(khuPho.getMaKhuPho());
        txfTenKhuPho.setText(khuPho.getTenKhuPho());
        danhSachHoDan = HoDan.getByMaKhuPho(khuPho.getMaKhuPho());
    }

    private void loadHoDan() {
        if (hoDanIndex < 0 || hoDanIndex >= danhSachHoDan.size()) {
            txfMaHoDan.setText("");
            txfSoNha.setText("");
            danhSachNguoi = new ArrayList<>();
        } else {
            HODAN hoDan = danhSachHoDan.get(hoDanIndex);
            txfMaHoDan.setText(hoDan.getMaHoDan());
            txfSoNha.setText(hoDan.getSoNha());
            danhSachNguoi = Nguoi.getByMaHoDan(hoDan.getMaHoDan());
        }
        if (hoDanIndex < danhSachHoDan.size() - 1) {
            btnHoDanSau.setEnabled(true);
        } else {
            btnHoDanSau.setEnabled(false);
        }
        if (hoDanIndex > 0) {
            btnHoDanTruoc.setEnabled(true);
        } else {
            btnHoDanTruoc.setEnabled(false);
        }
    }

    private void loadNguoi() {
        if (nguoiIndex < 0 || nguoiIndex >= danhSachNguoi.size()) {
            txfMaNguoi.setText("");
            txfHoTen.setText("");
            txfNamSinh.setText("");
            txfTuoi.setText("");
            txfNgheNghiep.setText("");
        } else {
            NGUOI nguoi = danhSachNguoi.get(nguoiIndex);
            txfMaNguoi.setText(nguoi.getMaNguoi());
            txfHoTen.setText(nguoi.getHoTen());
            txfNamSinh.setText(nguoi.getNamSinh() + "");
            txfTuoi.setText(nguoi.getTuoi() + "");
            txfNgheNghiep.setText(nguoi.getNgheNghiep());
        }
        if (nguoiIndex < danhSachNguoi.size() - 1) {
            btnThanhVienSau.setEnabled(true);
        } else {
            btnThanhVienSau.setEnabled(false);
        }
        if (nguoiIndex > 0) {
            btnThanhVienTruoc.setEnabled(true);
        } else {
            btnThanhVienTruoc.setEnabled(false);
        }
    }

    private void loadTable() {
        ArrayList<NGUOI> danhSach = Nguoi.getByMaKhuPho(maKhuPho);
        String[][] data = new String[danhSach.size()][6];
        for (int i = 0; i < danhSach.size(); i++) {
            data[i][0] = danhSach.get(i).getMaHoDan();
            data[i][1] = danhSach.get(i).getMaNguoi();
            data[i][2] = danhSach.get(i).getHoTen();
            data[i][3] = danhSach.get(i).getTuoi() + "";
            data[i][4] = danhSach.get(i).getNamSinh() + "";
            data[i][5] = danhSach.get(i).getNgheNghiep();
        }
        tblThanhVien.setModel(new DefaultTableModel(data, new String[]{"Mã hộ dân", "Mã người", "Họ tên", "Tuổi", "Năm sinh", "Nghề nghiệp"}));
        tblThanhVien.setTableHeader(null);
    }
