    private void loadTable() {
        ArrayList<KHUPHO> danhSach = KhuPho.getAll();
        String[][] data = new String[danhSach.size()][2];
        for (int i = 0; i < danhSach.size(); i++) {
            data[i][0] = danhSach.get(i).getMaKhuPho();
            data[i][1] = danhSach.get(i).getTenKhuPho();
        }
        tblKhuPho.setModel(new DefaultTableModel(data, new String[]{"Mã khu phố", "Tên khu phố"}));
        tblKhuPho.setTableHeader(null);
    }
