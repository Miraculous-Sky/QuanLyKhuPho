    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String maKhuPho = txfMaKhuPho.getText();
        int affectedRow = KhuPho.deleteKhuPho(maKhuPho);
        switch (affectedRow) {
            case -1:
                JOptionPane.showMessageDialog(this, "Có lỗi trong quá trình, hãy thử lại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                break;
            case 0:
                JOptionPane.showMessageDialog(this, "Mã khu phố không tồn tại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Xóa khu phố thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                break;
        }
        loadTable();
    }//GEN-LAST:event_btnXoaActionPerformed

